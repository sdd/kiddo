    #[cfg(feature = "rkyv_08")]
    #[test]
    fn rkyv_archived_vec_of_arenas_supports_queries() {
        type Tree = KdTree<f64, u32, EytzingerPf<3, 8>, VecOfArenas<f64, u32, 3, 32>, 3, 32>;
        type ArchivedTree =
            ArchivedKdTree<f64, u32, EytzingerPf<3, 8>, VecOfArenas<f64, u32, 3, 32>, 3, 32>;

        let points: Vec<[f64; 3]> = (0..4096)
            .map(|i| {
                [
                    (i % 257) as f64 / 257.0,
                    (i % 131) as f64 / 131.0,
                    (i % 67) as f64 / 67.0,
                ]
            })
            .collect();

        let tree = Tree::new_from_slice(&points).unwrap();
        let query = [0.321, 0.456, 0.789];
        let max_qty = NonZeroUsize::new(8).unwrap();
        let max_dist = 0.025;

        let bytes = rkyv_08::api::high::to_bytes_in::<_, rkyv_08::rancor::Error>(
            &tree,
            rkyv_08::util::AlignedVec::<128>::new(),
        )
        .unwrap();
        let archived =
            rkyv_08::access::<ArchivedTree, rkyv_08::rancor::Error>(bytes.as_slice()).unwrap();

        assert_eq!(
            archived
                .query(&query)
                .nearest_one::<SquaredEuclidean<f64>>()
                .approx()
                .execute(),
            tree.query(&query)
                .nearest_one::<SquaredEuclidean<f64>>()
                .approx()
                .execute()
        );
        assert_eq!(
            archived
                .query(&query)
                .nearest_one::<SquaredEuclidean<f64>>()
                .execute(),
            tree.query(&query)
                .nearest_one::<SquaredEuclidean<f64>>()
                .execute()
        );
        assert_eq!(
            archived
                .query(&query)
                .nearest_n::<SquaredEuclidean<f64>>(max_qty)
                .execute(),
            tree.query(&query)
                .nearest_n::<SquaredEuclidean<f64>>(max_qty)
                .execute()
        );
        assert_eq!(
            archived
                .query(&query)
                .nearest_n::<SquaredEuclidean<f64>>(max_qty)
                .within(max_dist)
                .exclusive_boundaries()
                .execute(),
            tree.query(&query)
                .nearest_n::<SquaredEuclidean<f64>>(max_qty)
                .within(max_dist)
                .exclusive_boundaries()
                .execute()
        );
        assert_eq!(
            archived
                .query(&query)
                .within::<SquaredEuclidean<f64>>(max_dist)
                .exclusive_boundaries()
                .execute(),
            tree.query(&query)
                .within::<SquaredEuclidean<f64>>(max_dist)
                .exclusive_boundaries()
                .execute()
        );
        assert_eq!(
            archived
                .query(&query)
                .within::<SquaredEuclidean<f64>>(max_dist)
                .execute(),
            tree.query(&query)
                .within::<SquaredEuclidean<f64>>(max_dist)
                .execute()
        );
        assert_eq!(
            archived
                .query(&query)
                .within::<SquaredEuclidean<f64>>(max_dist)
                .unsorted()
                .execute()
                .len(),
            tree.query(&query)
                .within::<SquaredEuclidean<f64>>(max_dist)
                .unsorted()
                .execute()
                .len()
        );
        assert_eq!(
            archived
                .query(&query)
                .within::<SquaredEuclidean<f64>>(max_dist)
                .exclusive_boundaries()
                .unsorted()
                .execute()
                .len(),
            tree.query(&query)
                .within::<SquaredEuclidean<f64>>(max_dist)
                .exclusive_boundaries()
                .unsorted()
                .execute()
                .len()
        );
        assert_eq!(
            archived
                .query(&query)
                .within::<SquaredEuclidean<f64>>(max_dist)
                .unsorted()
                .iter()
                .count(),
            tree.query(&query)
                .within::<SquaredEuclidean<f64>>(max_dist)
                .unsorted()
                .iter()
                .count()
        );
        assert_eq!(
            archived
                .query(&query)
                .within::<SquaredEuclidean<f64>>(max_dist)
                .exclusive_boundaries()
                .unsorted()
                .iter()
                .count(),
            tree.query(&query)
                .within::<SquaredEuclidean<f64>>(max_dist)
                .exclusive_boundaries()
                .unsorted()
                .iter()
                .count()
        );
        let archived_iter: Vec<_> = archived.iter().collect();
        let tree_iter: Vec<_> = tree.iter().collect();
        assert_eq!(archived_iter, tree_iter);
        assert_eq!(
            archived
                .query(&query)
                .best_n_within::<SquaredEuclidean<f64>>(max_dist, max_qty)
                .exclusive_boundaries()
                .execute()
                .into_sorted_vec(),
            tree.query(&query)
                .best_n_within::<SquaredEuclidean<f64>>(max_dist, max_qty)
                .exclusive_boundaries()
                .execute()
                .into_sorted_vec()
        );
        assert_eq!(
            archived
                .query(&query)
                .best_n_within::<SquaredEuclidean<f64>>(max_dist, max_qty)
                .execute()
                .into_sorted_vec(),
            tree.query(&query)
                .best_n_within::<SquaredEuclidean<f64>>(max_dist, max_qty)
                .execute()
                .into_sorted_vec()
        );
    }

