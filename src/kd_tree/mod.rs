    fn sort_entries_u32<A: Copy, const K: usize>(
        mut entries: Vec<(u32, [A; K])>,
    ) -> Vec<(u32, [A; K])> {
        entries.sort_by_key(|(item, _)| *item);
        entries
    }

    fn sort_entries_u16<A: Copy, const K: usize>(
        mut entries: Vec<(u16, [A; K])>,
    ) -> Vec<(u16, [A; K])> {
        entries.sort_by_key(|(item, _)| *item);
        entries
    }

